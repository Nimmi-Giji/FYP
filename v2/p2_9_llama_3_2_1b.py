def normalize_answer(s: str) -> str:
    s = s.lower()
    s = re.sub(r"\b(a|an|the)\b", " ", s)
    s = re.sub(r"[^a-z0-9 ]", "", s)
    return " ".join(s.split())


def token_f1(pred: str, gold: str) -> float:
    pred_toks = normalize_answer(pred).split()
    gold_toks = normalize_answer(gold).split()
    if not pred_toks or not gold_toks:
        return 0.0
    common   = Counter(pred_toks) & Counter(gold_toks)
    n_common = sum(common.values())
    if n_common == 0:
        return 0.0
    precision = n_common / len(pred_toks)
    recall    = n_common / len(gold_toks)
    return 2 * precision * recall / (precision + recall)


QA_PROMPT = "Answer briefly and directly.\nQuestion: {question}\nAnswer:"


def answer_question(mdl, tok, question: str) -> str:
    prompt = QA_PROMPT.format(question=question)
    inputs = tok(prompt, return_tensors="pt").to(DEVICE)
    mdl.eval()
    with torch.no_grad():
        out = mdl.generate(
            **inputs,
            max_new_tokens=30,
            do_sample=False,
            pad_token_id=tok.eos_token_id,
        )
    new_toks = out[0][inputs["input_ids"].shape[1]:]
    raw      = tok.decode(new_toks, skip_special_tokens=True).strip()
    # Take only first line/sentence to avoid padding
    return raw.split("\n")[0].split(".")[0].strip()


def evaluate_set(mdl, tok, qa_list: List[Dict]) -> float:
    """Average token-F1 over a list of {question, answer} dicts."""
    if not qa_list:
        return 0.0
    return float(np.mean([
        token_f1(answer_question(mdl, tok, item["question"]), item["answer"])
        for item in qa_list
    ]))


def evaluate_set_verbose(mdl, tok, qa_list: List[Dict]) -> float:
    """Same as evaluate_set but prints each prediction."""
    if not qa_list:
        return 0.0
    scores = []
    for item in qa_list:
        pred = answer_question(mdl, tok, item["question"])
        f1   = token_f1(pred, item["answer"])
        scores.append(f1)
        print(f"    Q : {item['question'][:80]}")
        print(f"    A Correct: {item['answer'][:60]}")
        print(f"    A Predict: {pred[:60]}   F1={f1:.3f}")
    return float(np.mean(scores))


def evaluate_synth_edit(mdl, tok, synth: Dict) -> float:
    """Evaluate model on the QA pairs inside a synthetic edit."""
    return evaluate_set(mdl, tok, synth.get("qa_pairs", []))


def adapter_drift(params_before: List[torch.Tensor],
                  params_after:  List[torch.Tensor]) -> float:
    delta = torch.cat([(a - b).flatten()
                       for a, b in zip(params_after, params_before)])
    return delta.norm().item()


def lora_params_snapshot(mdl) -> List[torch.Tensor]:
    return [p.detach().clone()
            for n, p in mdl.named_parameters() if p.requires_grad]


def acceptance_score(es: float, forget: float, drift: float) -> float:
    return ALPHA * es - BETA * forget - GAMMA * drift


