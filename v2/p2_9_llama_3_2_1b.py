def build_synth_prompt(passage: str) -> str:
    return SYNTH_PROMPT_TEMPLATE.format(passage=passage[:800].strip())


def try_parse_json(text: str) -> Optional[Dict]:
    # 1. Strip markdown fences
    text = re.sub(r"```(?:json)?", "", text).strip()

    # 2. Direct parse
    try:
        obj = json.loads(text)
        if "paraphrases" in obj and "qa_pairs" in obj:
            return obj
    except Exception:
        pass

    # 3. Outermost { ... } block
    start = text.find("{")
    end   = text.rfind("}")
    if start != -1 and end != -1 and end > start:
        snippet = text[start:end+1]
        try:
            obj = json.loads(snippet)
            if "paraphrases" in obj and "qa_pairs" in obj:
                return obj
        except Exception:
            pass

        # 4. Fix trailing commas + single quotes
        snippet = re.sub(r",\s*([}\]])", r"\1", snippet)
        snippet = snippet.replace("'", '"')
        try:
            obj = json.loads(snippet)
            if "paraphrases" in obj and "qa_pairs" in obj:
                return obj
        except Exception:
            pass

    # 5. Manual regex fallback
    search_text = text[start:end+1] if start != -1 and end != -1 else text
    paraphrases = re.findall(r'"((?:[^"\\]|\\.){10,})"', search_text)
    questions   = re.findall(r'"question"\s*:\s*"([^"]+)"', search_text, re.IGNORECASE)
    answers     = re.findall(r'"answer"\s*:\s*"([^"]+)"',   search_text, re.IGNORECASE)
    qa_pairs    = [{"question": q, "answer": a} for q, a in zip(questions, answers)]

    if paraphrases or qa_pairs:
        return {"paraphrases": paraphrases[:5], "qa_pairs": qa_pairs[:5]}

    return None


def generate_synthetic_edits(
    passage: str,
    n_samples: int = BATCH_SIZE_EDITS,
    debug: bool = False,
) -> List[Dict]:
    prompt = build_synth_prompt(passage)

    if hasattr(tokenizer, "apply_chat_template"):
        messages  = [{"role": "user", "content": prompt}]
        formatted = tokenizer.apply_chat_template(
            messages, tokenize=False, add_generation_prompt=True
        )
        inputs = tokenizer(formatted, return_tensors="pt").to(DEVICE)
    else:
        inputs = tokenizer(prompt, return_tensors="pt").to(DEVICE)

    results  = []
    failures = 0
    model.eval()

    for i in range(n_samples):
        with torch.no_grad():
            out = model.generate(
                **inputs,
                max_new_tokens=MAX_NEW_TOKENS,
                temperature=GEN_TEMPERATURE,
                do_sample=True,
                pad_token_id=tokenizer.eos_token_id,
                repetition_penalty=1.1,
            )
        new_tokens = out[0][inputs["input_ids"].shape[1]:]
        raw        = tokenizer.decode(new_tokens, skip_special_tokens=True).strip()
        parsed     = try_parse_json(raw)

        if parsed:
            parsed["paraphrases"] = [
                s for s in parsed.get("paraphrases", [])
                if isinstance(s, str) and len(s) > 5
            ]
            parsed["qa_pairs"] = [
                qa for qa in parsed.get("qa_pairs", [])
                if isinstance(qa, dict) and qa.get("question") and qa.get("answer")
            ]
            if parsed["paraphrases"] or parsed["qa_pairs"]:
                results.append(parsed)
            else:
                failures += 1
                if debug:
                    print(f"  [sample {i+1}] Parsed but empty.\n  RAW: {raw[:300]}\n")
        else:
            failures += 1
            if debug:
                print(f"  [sample {i+1}] Parse failed.\n  RAW:\n{raw[:500]}\n{'─'*40}")

    print(f"  Synthetic edits: {len(results)} OK / {failures} failed out of {n_samples}")
    return results


