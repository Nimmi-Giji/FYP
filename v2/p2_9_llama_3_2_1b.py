def compute_retention_gradient_matrix(
    mdl, tok, ret_set: List[Dict], k: int = NULL_PROJ_K
) -> Tuple[torch.Tensor, List[str]]:

    lora_param_list = [(n, p) for n, p in mdl.named_parameters() if p.requires_grad]
    pnames    = [n for n, _ in lora_param_list]

    G = []
    mdl.train()

    for i, item in enumerate(ret_set):
        prompt = QA_PROMPT.format(question=item["question"])
        full   = prompt + " " + item["answer"]
        enc    = tok(full, return_tensors="pt", truncation=True, max_length=256).to(DEVICE)
        labels = enc["input_ids"].clone()

        try:
            out  = mdl(**enc, labels=labels)
            loss = out.loss

            if not torch.isfinite(loss):
                print(f"  [retention {i}] non-finite loss — skipping")
                mdl.zero_grad()
                continue

            mdl.zero_grad()
            loss.backward()

            grad_vec = torch.cat([
                p.grad.detach().float().flatten()
                if (p.grad is not None and torch.isfinite(p.grad).all())
                else torch.zeros(p.numel(), dtype=torch.float32, device=DEVICE)
                for _, p in lora_param_list
            ])

            if not torch.isfinite(grad_vec).all():
                print(f"  [retention {i}] NaN in grad — skipping")
                mdl.zero_grad()
                continue

            grad_vec = torch.clamp(grad_vec, -1.0, 1.0)
            G.append(grad_vec)

        except Exception as e:
            print(f"  [retention {i}] error: {e} — skipping")
            mdl.zero_grad()
            continue

        mdl.zero_grad()

    mdl.eval()
    print(f"  Valid retention gradients: {len(G)}/{len(ret_set)}")

    if len(G) == 0:
        raise RuntimeError("No valid retention gradients collected.")

    G_mat    = torch.stack(G).float()
    k_actual = min(k, G_mat.shape[0], G_mat.shape[1])

    # SVD on CPU to avoid cusolver NaN issues
    _, _, Vt = torch.linalg.svd(G_mat.cpu(), full_matrices=False)
    U_k      = Vt[:k_actual].T   # (d_adapter, k_actual)

    print(f"  U_k shape: {U_k.shape}")
    return U_k.to(DEVICE), pnames


