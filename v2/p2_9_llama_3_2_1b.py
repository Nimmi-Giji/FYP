def distribution_plot(ax, c_vals, u_vals, xlabel, title):
    """Overlapping histograms."""
    bins = np.linspace(
        min(min(c_vals, default=0), min(u_vals, default=0)),
        max(max(c_vals, default=0), max(u_vals, default=0)),
        20
    )
    ax.hist(c_vals, bins=bins, color=C_COLOR, alpha=0.55, label="Constrained",   edgecolor="white")
    ax.hist(u_vals, bins=bins, color=U_COLOR, alpha=0.55, label="Unconstrained", edgecolor="white")
    ax.axvline(np.mean(c_vals), color=C_COLOR, linewidth=2, linestyle="--")
    ax.axvline(np.mean(u_vals), color=U_COLOR, linewidth=2, linestyle="--")
    ax.set_title(title, fontweight="bold")
    ax.set_xlabel(xlabel)
    ax.set_ylabel("Count")
    ax.legend(fontsize=9)


