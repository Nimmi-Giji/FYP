def project_to_null_space(grad_vec: torch.Tensor, U_k: torch.Tensor) -> torch.Tensor:
    """Remove components of grad_vec that lie in the retention subspace."""
    U_k_f = U_k.to(grad_vec.dtype)
    proj   = U_k_f @ (U_k_f.T @ grad_vec)
    return grad_vec - proj


def apply_gradient_update(mdl, grad_vec: torch.Tensor, lr: float = EDIT_LR):
    """Apply a flat gradient vector to LoRA parameters."""
    offset = 0
    for n, p in mdl.named_parameters():
        if not p.requires_grad:
            continue
        numel   = p.numel()
        g_slice = grad_vec[offset:offset+numel].reshape(p.shape).to(p.dtype)
        with torch.no_grad():
            p -= lr * g_slice
        offset += numel


