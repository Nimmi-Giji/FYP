def bar_pair(ax, c_val, u_val, c_err, u_err, ylabel, title, higher_better=True):
    bars = ax.bar(
        ["Constrained", "Unconstrained"],
        [c_val, u_val],
        yerr=[c_err, u_err],
        color=[C_COLOR, U_COLOR],
        alpha=ALPHA_BAR,
        capsize=6,
        width=0.5,
        edgecolor="white",
    )
    ax.set_title(title, fontweight="bold")
    ax.set_ylabel(ylabel)
    ax.axhline(0, color="gray", linewidth=0.8, linestyle="--")
    # annotate
    for bar, val in zip(bars, [c_val, u_val]):
        ax.text(
            bar.get_x() + bar.get_width() / 2,
            bar.get_height() + (0.003 if val >= 0 else -0.012),
            f"{val:.4f}",
            ha="center", va="bottom", fontsize=10, fontweight="bold"
        )
    direction = "↑ better" if higher_better else "↓ better"
    ax.text(0.97, 0.97, direction, transform=ax.transAxes,
            ha="right", va="top", fontsize=9, color="gray")


def trace_plot(ax, c_vals, u_vals, ylabel, title, higher_better=True):
    x_c = range(len(c_vals))
    x_u = range(len(u_vals))
    ax.plot(x_c, c_vals, color=C_COLOR, linewidth=2,   label="Constrained",   marker="o", markersize=4)
    ax.plot(x_u, u_vals, color=U_COLOR, linewidth=2,   label="Unconstrained", marker="s", markersize=4)
    # rolling mean
    def rolling(vals, w=5):
        return [np.mean(vals[max(0,i-w):i+1]) for i in range(len(vals))]
    ax.plot(x_c, rolling(c_vals), color=C_COLOR, linewidth=1.5, linestyle="--", alpha=0.6)
    ax.plot(x_u, rolling(u_vals), color=U_COLOR, linewidth=1.5, linestyle="--", alpha=0.6)
    ax.axhline(0, color="gray", linewidth=0.8, linestyle=":")
    ax.set_title(title, fontweight="bold")
    ax.set_xlabel("Edit sample index")
    ax.set_ylabel(ylabel)
    ax.legend(fontsize=9)
    direction = "↑ better" if higher_better else "↓ better"
    ax.text(0.97, 0.97, direction, transform=ax.transAxes,
            ha="right", va="top", fontsize=9, color="gray")


