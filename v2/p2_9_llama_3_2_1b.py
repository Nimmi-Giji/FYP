def build_edit_corpus(synth_edit: Dict) -> List[str]:
    texts = []
    texts.extend(synth_edit.get("paraphrases", []))
    for qa in synth_edit.get("qa_pairs", []):
        q = qa.get("question", "")
        a = qa.get("answer", "")
        if q and a:
            texts.append(f"Question: {q}\nAnswer: {a}")
    return texts


def compute_edit_gradient(
    mdl, tok, synth_edit: Dict, steps: int = EDIT_STEPS
) -> Optional[torch.Tensor]:

    corpus = build_edit_corpus(synth_edit)
    if not corpus:
        return None

    lora_params = [(n, p) for n, p in mdl.named_parameters() if p.requires_grad]
    d_adapter   = sum(p.numel() for _, p in lora_params)
    accum_grad  = torch.zeros(d_adapter, device=DEVICE, dtype=torch.float32)

    mdl.train()
    for step in range(steps):
        for text in corpus:
            enc = tok(
                text, return_tensors="pt",
                truncation=True, max_length=256
            ).to(DEVICE)
            labels = enc["input_ids"].clone()
            out    = mdl(**enc, labels=labels)
            loss   = out.loss / (len(corpus) * steps)

            if not torch.isfinite(loss):
                continue

            loss.backward()

        g = torch.cat([
            p.grad.detach().float().flatten()
            if (p.grad is not None and torch.isfinite(p.grad).all())
            else torch.zeros(p.numel(), dtype=torch.float32, device=DEVICE)
            for _, p in lora_params
        ])
        accum_grad += g
        mdl.zero_grad()

    mdl.eval()

    if not torch.isfinite(accum_grad).all():
        return None

    return accum_grad


