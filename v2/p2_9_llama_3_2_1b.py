def run_edit_loop(
    mdl, tok, fact: Dict,
    U_k: torch.Tensor,
    retention_qa: List[Dict],
    n_synth: int = BATCH_SIZE_EDITS,
    use_projection: bool = True,
) -> List[Dict]:

    passage = fact["passage"]
    label   = "CONSTRAINED" if use_projection else "UNCONSTRAINED"

    print(f"\n[{label}] Fact: {fact['title'][:50]}")
    print(f"  Generating {n_synth} synthetic edits...")
    synth_edits = generate_synthetic_edits(passage, n_samples=n_synth)

    if not synth_edits:
        print("  No valid synthetic edits generated — skipping.")
        return []

    f1_retention_before = evaluate_set(mdl, tok, retention_qa)
    results = []

    for idx, synth in enumerate(synth_edits):
        print(f"\n  Edit sample {idx+1}/{len(synth_edits)}")

        # Measure ES on the synth QA pairs before the update
        f1_edit_before = evaluate_synth_edit(mdl, tok, synth)
        backup         = lora_params_snapshot(mdl)

        g = compute_edit_gradient(mdl, tok, synth)
        if g is None:
            print("    Bad gradient — skipping.")
            continue

        if use_projection:
            g = project_to_null_space(g, U_k)

        apply_gradient_update(mdl, g, lr=EDIT_LR)

        # Measure ES on the same synth QA pairs after the update
        f1_edit_after      = evaluate_synth_edit(mdl, tok, synth)
        f1_retention_after = evaluate_set(mdl, tok, retention_qa)
        params_after       = lora_params_snapshot(mdl)

        es     = f1_edit_after - f1_edit_before
        forget = max(0.0, f1_retention_before - f1_retention_after)
        drift  = adapter_drift(backup, params_after)
        score  = acceptance_score(es, forget, drift)

        # Unconstrained: always accept (raw baseline)
        # Constrained:   accept if score >= TAU
        if use_projection:
            accept = score >= TAU
        else:
            accept = True

        print(f"BR={f1_retention_after:.3f}  Forget={forget:.4f}  "
              f"Drift={drift:.4f}  Score={score:.4f}  "
              f" {'ACCEPT' if accept else 'REJECT'}")

        if not accept:
            with torch.no_grad():
                for (n, p), b in zip(
                    [(n, p) for n, p in mdl.named_parameters() if p.requires_grad],
                    backup
                ):
                    p.copy_(b)

        results.append({
            "sample_idx":     idx,
            "projection":     use_projection,
            "es":             es,
            "f1_edit_before": f1_edit_before,
            "f1_edit_after":  f1_edit_after,
            "br_before":      f1_retention_before,
            "br_after":       f1_retention_after,
            "forget":         forget,
            "drift":          drift,
            "score":          score,
            "accepted":       accept,
        })

    return results


