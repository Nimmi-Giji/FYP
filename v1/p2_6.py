def get_lora_params(model) -> List[torch.Tensor]:
    """Extract all LoRA parameters from model."""
    lora_params = []
    for name, param in model.named_parameters():
        if 'lora' in name and param.requires_grad:
            lora_params.append(param)
    return lora_params

def flatten_gradients(params: List[torch.Tensor]) -> torch.Tensor:
    """Flatten gradients from parameter list into 1D vector."""
    grads = []
    for p in params:
        if p.grad is not None:
            grads.append(p.grad.detach().flatten())
    return torch.cat(grads)

def unflatten_gradients(flat_grad: torch.Tensor, params: List[torch.Tensor]):
    """Unflatten 1D gradient vector back to parameter shapes."""
    offset = 0
    for p in params:
        numel = p.numel()
        p.grad = flat_grad[offset:offset + numel].view_as(p).clone()
        offset += numel

def compute_retention_basis(model, tokenizer, retention_set, k: int = 20) -> torch.Tensor:
    """
    Compute low-rank basis spanning retention gradient subspace.

    Args:
        model: PEFT model with LoRA adapters
        tokenizer: Tokenizer
        retention_set: Dataset to preserve
        k: Rank of subspace (number of principal components)

    Returns:
        U_k: Orthonormal basis matrix (d_adapter × k)
    """
    print(f"Building retention basis (k={k})...")

    lora_params = get_lora_params(model)
    d_adapter = sum(p.numel() for p in lora_params)
    print(f"Total LoRA parameters: {d_adapter:,}")

    # Collect gradients
    G = []  # Will be N × d_adapter

    model.train()
    for item in tqdm(retention_set, desc="Computing retention gradients"):
        # Format input
        prompt = format_qa_prompt(item['question'], item['context'])
        target = item['answers']['text'][0]
        full_text = prompt + " " + target

        # Tokenize
        inputs = tokenizer(full_text, return_tensors="pt", truncation=True, max_length=512)
        inputs = {k: v.to(model.device) for k, v in inputs.items()}

        # Forward pass
        outputs = model(**inputs, labels=inputs['input_ids'])
        loss = outputs.loss

        # Backward pass
        model.zero_grad()
        loss.backward()

        # Collect flattened gradient
        g = flatten_gradients(lora_params)
        G.append(g.cpu())

    # Stack into matrix: N × d_adapter
    G = torch.stack(G)
    print(f"Gradient matrix shape: {G.shape}")

    # Compute truncated SVD: G ≈ U_k Σ_k V_k^T
    # We only need U_k (left singular vectors)
    U, S, Vh = torch.linalg.svd(G, full_matrices=False)

    # Take top k components
    k = min(k, U.shape[1])  # Handle case where k > rank
    U_k = Vh[:k, :].T  # d_adapter × k

    print(f"Retained singular values: {S[:k].tolist()[:5]}...")
    print(f"Basis shape: {U_k.shape}")
    print(f"Explained variance ratio: {(S[:k]**2).sum() / (S**2).sum():.4f}")

    return U_k.to(model.device)

