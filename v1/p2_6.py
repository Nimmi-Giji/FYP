def project_to_null_space(grad_flat: torch.Tensor, basis: torch.Tensor) -> torch.Tensor:
    """
    Project gradient to null space of retention basis.

    g_proj = g - U_k (U_k^T g)

    Args:
        grad_flat: Flattened gradient vector (d_adapter,)
        basis: Orthonormal basis U_k (d_adapter × k)

    Returns:
        Projected gradient (d_adapter,)
    """
    # Project onto subspace
    projection = basis @ (basis.T @ grad_flat)

    # Remove projection (keep orthogonal component)
    grad_projected = grad_flat - projection

    return grad_projected

def compute_edit_gradient(model, tokenizer, item: Dict) -> torch.Tensor:
    """
    Compute gradient for a single edit example.

    Args:
        model: PEFT model
        tokenizer: Tokenizer
        item: SQuAD-format dict with question, context, answers

    Returns:
        Flattened gradient vector
    """
    lora_params = get_lora_params(model)

    # Format input
    prompt = format_qa_prompt(item['question'], item['context'])
    target = item['answers']['text'][0]
    full_text = prompt + " " + target

    # Tokenize
    inputs = tokenizer(full_text, return_tensors="pt", truncation=True, max_length=512)
    inputs = {k: v.to(model.device) for k, v in inputs.items()}

    # Forward + backward
    model.zero_grad()
    outputs = model(**inputs, labels=inputs['input_ids'])
    loss = outputs.loss
    loss.backward()

    # Extract gradient
    grad = flatten_gradients(lora_params)

    return grad

## Safety Module
def check_output_safety(text: str) -> Tuple[bool, str]:
    """Check if generated text is degenerate or invalid."""
    text_stripped = text.strip()

    # Check for output collapse
    if len(text_stripped) < 3:
        return False, "Output too short (degenerate)"

    # Check for repetitive output (sign of degradation)
    words = text_stripped.split()
    if len(words) > 5:
        # Check if same word repeats more than 50% of output
        word_counts = {}
        for word in words:
            word_counts[word] = word_counts.get(word, 0) + 1
        max_count = max(word_counts.values())
        if max_count / len(words) > 0.5:
            return False, "Repetitive output detected"

    return True, "OK"

def check_behavioral_safety(
    model,
    tokenizer,
    safety_set,
    baseline_em: float,
    baseline_f1: float,
    em_threshold: float = 10.0,  # Allow max 10 point EM drop
    f1_threshold: float = 10.0   # Allow max 10 point F1 drop
) -> Tuple[bool, str]:
    """
    Check if model performance on safety-critical examples is preserved.
    This prevents edits that would cause performance degradation.

    Args:
        model: Model after edit
        tokenizer: Tokenizer
        safety_set: Critical QA examples to check performance
        baseline_em: Baseline exact match score
        baseline_f1: Baseline F1 score
        em_threshold: Max allowed EM drop (in percentage points)
        f1_threshold: Max allowed F1 drop (in percentage points)

    Returns:
        (is_safe, reason)
    """
    current_metrics = compute_em_f1(model, tokenizer, safety_set)
    current_em = current_metrics['exact_match']
    current_f1 = current_metrics['f1']

    em_drop = baseline_em - current_em
    f1_drop = baseline_f1 - current_f1

    if em_drop > em_threshold:
        return False, f"Performance degraded: EM dropped by {em_drop:.2f} points (threshold: {em_threshold})"

    if f1_drop > f1_threshold:
        return False, f"Performance degraded: F1 dropped by {f1_drop:.2f} points (threshold: {f1_threshold})"

    return True, "Performance preserved"

## Batch Processing
@dataclass
class EditCandidate:
    """Represents a single edit candidate."""
    item: Dict
    gradient: torch.Tensor
    projected_gradient: torch.Tensor
    score: float = 0.0
    safe: bool = True
    safety_reason: str = "Not checked"

def backup_lora_state(model) -> Dict:
    """Backup current LoRA adapter weights."""
    state = {}
    for name, param in model.named_parameters():
        if 'lora' in name and param.requires_grad:
            state[name] = param.data.clone()
    return state

def restore_lora_state(model, state: Dict):
    """Restore LoRA adapter weights from backup."""
    for name, param in model.named_parameters():
        if name in state:
            param.data.copy_(state[name])

def apply_projected_update(
    model,
    projected_grad: torch.Tensor,
    lr: float = 1e-5
):
    """
    Apply projected gradient update to LoRA parameters.

    Args:
        model: PEFT model
        projected_grad: Flattened projected gradient
        lr: Learning rate for update
    """
    lora_params = get_lora_params(model)
    unflatten_gradients(projected_grad, lora_params)

    # Apply gradient descent step
    with torch.no_grad():
        for p in lora_params:
            if p.grad is not None:
                p.data.add_(p.grad, alpha=-lr)

def compute_adapter_drift(state_before: Dict, state_after: Dict) -> float:
    """Compute L2 norm of parameter change."""
    total_drift = 0.0
    for name in state_before:
        if name in state_after:
            diff = state_after[name] - state_before[name]
            total_drift += (diff ** 2).sum().item()
    return np.sqrt(total_drift)

def score_edit(
    model,
    tokenizer,
    edit_item: Dict,
    retention_set,
    baseline_retention_em: float,
    baseline_edit_em: float,
    state_before: Dict,
    lambda_forget: float = 1.0,
    lambda_drift: float = 0.01
) -> float:
    """
    Compute acceptance score for an edit.

    S = EditGain - λ₁·Forget - λ₂·||Δθ||
    """
    # Measure edit success (on single item for efficiency)
    current_answer = generate_answer(
        model, tokenizer,
        edit_item['question'],
        edit_item['context']
    )
    target_answer = edit_item['answers']['text'][0]

    # Simple word overlap as proxy for improvement
    current_words = set(current_answer.lower().split())
    target_words = set(target_answer.lower().split())
    overlap = len(current_words & target_words) / max(len(target_words), 1)
    edit_gain = overlap

    # Measure forgetting (sample retention set for speed)
    sample_size = min(20, len(retention_set))
    retention_sample = retention_set.select(range(sample_size))
    current_retention = compute_em_f1(model, tokenizer, retention_sample)
    forget_score = max(0, baseline_retention_em - current_retention['exact_match'])

    # Measure adapter drift
    state_after = backup_lora_state(model)
    drift = compute_adapter_drift(state_before, state_after)

    # Compute total score
    score = edit_gain - lambda_forget * forget_score - lambda_drift * drift

    return score

#### MAIN LOOP
def process_edit_batch(
    model,
    tokenizer,
    edit_batch,
    retention_set,
    safety_set,
    retention_basis: torch.Tensor,
    baseline_retention_em: float,
    baseline_retention_f1: float,
    baseline_safety_em: float,
    baseline_safety_f1: float,
    acceptance_threshold: float = 0.1,
    lr: float = 1e-5
) -> Dict:
    """
    Process a batch of edits with null-space projection and performance-based safety filtering.

    Safety filtering prevents:
    1. Edits that cause output degradation (degenerate outputs)
    2. Edits that significantly hurt performance on retention/safety sets

    Rollback facility:
    - Every edit is applied to a temporary backup
    - If safety checks fail, weights are restored
    - Only accepted edits persist

    Returns:
        Dictionary with results and metrics
    """
    results = {
        'total': len(edit_batch),
        'accepted': 0,
        'rejected_safety': 0,
        'rejected_score': 0,
        'candidates': []
    }

    model.train()

    # Phase 1: Generate and project gradients
    print("\n=== Phase 1: Gradient Computation & Projection ===")
    candidates = []

    for item in tqdm(edit_batch, desc="Computing edit gradients"):
        # Compute raw gradient
        grad = compute_edit_gradient(model, tokenizer, item)

        # Project to null space
        grad_proj = project_to_null_space(grad, retention_basis)

        candidate = EditCandidate(
            item=item,
            gradient=grad,
            projected_gradient=grad_proj
        )
        candidates.append(candidate)

    # Phase 2: Pre-edit output quality checks
    print("\n=== Phase 2: Pre-Edit Output Quality Checks ===")
    for candidate in tqdm(candidates, desc="Checking output quality"):
        # Generate answer with current model
        answer = generate_answer(
            model, tokenizer,
            candidate.item['question'],
            candidate.item['context']
        )

        # Check for degenerate outputs
        safe, reason = check_output_safety(answer)
        candidate.safe = safe
        candidate.safety_reason = reason

        if not safe:
            results['rejected_safety'] += 1
            print(f"  ✗ Pre-edit rejection: {reason}")

    # Phase 3: Apply, test, and score edits (WITH ROLLBACK)
    print("\n=== Phase 3: Edit Application with Performance Safety & Rollback ===")
    for i, candidate in enumerate(tqdm(candidates, desc="Testing edits")):
        if not candidate.safe:
            continue

        # Rollback - Backup state before edit
        state_backup = backup_lora_state(model)

        # Apply projected update temporarily
        apply_projected_update(model, candidate.projected_gradient, lr=lr)

        # SAFETY CHECK 1: Performance on safety set
        safe, reason = check_behavioral_safety(
            model, tokenizer, safety_set,
            baseline_safety_em,
            baseline_safety_f1,
            em_threshold=10.0,  # Max 10 point drop
            f1_threshold=10.0
        )

        if not safe:
            candidate.safe = False
            candidate.safety_reason = reason
            results['rejected_safety'] += 1
            # Rollback -  Restore previous weights
            restore_lora_state(model, state_backup)
            print(f"  ✗ Edit {i+1}: ROLLED BACK - {reason}")
            continue

        # SAFETY CHECK 2: Performance on retention set
        retention_sample = retention_set.select(range(min(20, len(retention_set))))
        retention_check = compute_em_f1(model, tokenizer, retention_sample)
        retention_drop = baseline_retention_em - retention_check['exact_match']

        if retention_drop > 15.0:  # Max 15 point drop on retention
            candidate.safe = False
            candidate.safety_reason = f"Retention performance dropped by {retention_drop:.2f} points"
            results['rejected_safety'] += 1
            # Rollback -  Restore previous weights
            restore_lora_state(model, state_backup)
            print(f"  ✗ Edit {i+1}: ROLLED BACK - Retention degradation")
            continue

        #  SAFETY CHECK 3: Output quality after edit
        test_answer = generate_answer(
            model, tokenizer,
            candidate.item['question'],
            candidate.item['context']
        )
        safe, reason = check_output_safety(test_answer)

        if not safe:
            candidate.safe = False
            candidate.safety_reason = f"Post-edit output degraded: {reason}"
            results['rejected_safety'] += 1
            # Rollback -  Restore previous weights
            restore_lora_state(model, state_backup)
            print(f"  ✗ Edit {i+1}: ROLLED BACK - Output degradation")
            continue

        # safety checks passed, now score the edit
        score = score_edit(
            model, tokenizer, candidate.item,
            retention_set, baseline_retention_em,
            baseline_edit['exact_match'],
            state_backup
        )
        candidate.score = score

        # Accept or reject based on score threshold
        if score > acceptance_threshold:
            results['accepted'] += 1
            print(f"Edit {i+1}: ACCEPTED (score={score:.3f})")
            # Edit is kept - no rollback
        else:
            results['rejected_score'] += 1
            # Rollback -  Restore previous weights
            restore_lora_state(model, state_backup)
            print(f"Edit {i+1}: ROLLED BACK - Low score ({score:.3f})")

    results['candidates'] = candidates
    return results

