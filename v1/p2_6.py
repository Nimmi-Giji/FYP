def format_qa_prompt(question: str, context: str = "") -> str:
    """Format question as a prompt for the model."""
    if context:
        return f"Context: {context}\n\nQuestion: {question}\n\nAnswer:"
    return f"Question: {question}\n\nAnswer:"

def extract_answer(text: str) -> str:
    """Extract answer from model output."""
    # Simple heuristic: take text after "Answer:" and before newline
    if "Answer:" in text:
        answer = text.split("Answer:")[-1].strip()
    else:
        answer = text.strip()

    # Take first sentence or up to 50 tokens
    answer = answer.split('\n')[0].strip()
    tokens = answer.split()
    if len(tokens) > 50:
        answer = ' '.join(tokens[:50])

    return answer

def generate_answer(model, tokenizer, question: str, context: str = "", max_new_tokens: int = 50) -> str:
    """Generate answer for a question."""
    prompt = format_qa_prompt(question, context)
    inputs = tokenizer(prompt, return_tensors="pt", padding=True, truncation=True, max_length=512)
    inputs = {k: v.to(model.device) for k, v in inputs.items()}

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            pad_token_id=tokenizer.eos_token_id
        )

    full_text = tokenizer.decode(outputs[0], skip_special_tokens=True)
    return extract_answer(full_text)

